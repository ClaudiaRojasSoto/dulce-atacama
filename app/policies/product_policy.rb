class ProductPolicy < ApplicationPolicy
  def index?
    true
  end
  
  def show?
    true
  end
  
  def create?
    user&.admin?
  end
  
  def update?
    user&.admin?
  end
  
  def destroy?
    user&.admin?
  end
  
  class Scope < Scope
    def resolve
      if user&.admin?
        scope.all
      else
        scope.active
      end
    end
  end
end

